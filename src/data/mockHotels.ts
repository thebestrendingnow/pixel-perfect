// Country aliases for flexible matching
const COUNTRY_ALIASES: Record<string, string[]> = {
  'netherlands': ['nl', 'nederland', 'holland', 'the netherlands'],
  'belgium': ['be', 'belgië', 'belgique'],
  'germany': ['de', 'deutschland', 'german'],
  'france': ['fr', 'french'],
  'united kingdom': ['uk', 'gb', 'england', 'britain', 'great britain'],
  'spain': ['es', 'españa', 'espana', 'spanish'],
  'usa': ['us', 'united states', 'america', 'american'],
};

export function searchMockHotels(query: string, filters: {
  minPrice?: number;
  maxPrice?: number;
  minStars?: number;
  amenities?: string[];
} = {}): Hotel[] {
  let results = [...mockHotels];
  const q = query.toLowerCase().trim();

  if (q) {
    // Direct city/country/name match first
    let matched = results.filter(h =>
      h.city.toLowerCase().includes(q) ||
      h.country.toLowerCase().includes(q) ||
      h.name.toLowerCase().includes(q) ||
      h.address.toLowerCase().includes(q)
    );

    // Try country aliases (nl → Netherlands, etc.)
    if (matched.length === 0) {
      for (const [country, aliases] of Object.entries(COUNTRY_ALIASES)) {
        if (aliases.includes(q) || country.includes(q)) {
          matched = results.filter(h => h.country.toLowerCase() === country);
          break;
        }
      }
    }

    results = matched.length > 0 ? matched : results;
  }

  if (filters.minPrice !== undefined) results = results.filter(h => h.pricePerNight >= filters.minPrice!);
  if (filters.maxPrice !== undefined) results = results.filter(h => h.pricePerNight <= filters.maxPrice!);
  if (filters.minStars !== undefined && filters.minStars > 0) results = results.filter(h => h.stars >= filters.minStars!);
  if (filters.amenities?.length) {
    results = results.filter(h => filters.amenities!.every(a => h.amenities.includes(a)));
  }

  return results;
}

