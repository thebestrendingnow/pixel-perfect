/**
 * Detect locale from browser language settings.
 * BACKEND NOTE: Replace this with IP-based detection from GET /api/locale
 */
export function detectLocale(): Locale {
  const browserLang = navigator.language?.split('-')[0]?.toLowerCase() ?? 'en';
  if (browserLang in translations) return browserLang as Locale;
  return 'en';
}

export function t(key: string, locale: Locale = 'en'): string {
  return translations[locale]?.[key] ?? translations.en[key] ?? key;
}

export function getCurrencyForLocale(locale: Locale): string {
  const map: Record<Locale, string> = {
    en: 'USD', es: 'EUR', fr: 'EUR', de: 'EUR', pt: 'BRL', ja: 'JPY', zh: 'CNY', ar: 'SAR',
  };
  return map[locale] ?? 'USD';
}
